package br.cucumbercourse.entities;

public class Movie {

	private int stock;
	private int price;
	
	public void setStock(int arg1) {
		this.stock = arg1;
		
	}

	public void setPrice(int arg1) {
		this.price = arg1;
		
	}

	public int getPrice() {
		return price;
	}

	public int getStock() {
		return stock;
	}

	

}
